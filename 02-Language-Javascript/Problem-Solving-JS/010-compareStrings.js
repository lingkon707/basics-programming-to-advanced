  function compareStrings(str1, str2) {
    if (str1 === str2) {
        return "Strings are equal";
    } else {
        return "Strings are different";
    }
}

// Example usage:
const string1 = "hello";
const string2 = "world";
console.log(compareStrings(string1, string2)); // Output: Strings are different
