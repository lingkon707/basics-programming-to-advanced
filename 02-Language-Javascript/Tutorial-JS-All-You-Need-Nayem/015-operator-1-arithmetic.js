// arithmetic operators
// + (addition), - (subtraction), * (multiplication), / (division), and % (reminder-modulus)
//   **Exponentiation || ++Increment || --Decrement

console.log("use case of reminder-modulus");
var totalMango = 50;
var totalFriend = 6;
console.log("Last Mango: " + (totalMango % totalFriend));

console.log("Pre Increment - section below");
var firstIncrement = 200;
console.log(firstIncrement++); // 200
console.log(firstIncrement++);
console.log(firstIncrement++);
console.log(firstIncrement++);

console.log("Post Increment - section below");
var secondIncrement = 200;
console.log(++secondIncrement); // 201
console.log(++secondIncrement);
console.log(++secondIncrement);
console.log(++secondIncrement);

console.log("Pre Decrement - section below");
var firstDecrement = 400;
console.log(firstDecrement--); // 400
console.log(firstDecrement--);
console.log(firstDecrement--);
console.log(firstDecrement--);

console.log("Post Decrement - section below");
var secondIncrement = 400;
console.log(--secondIncrement); // 399
console.log(--secondIncrement);
console.log(--secondIncrement);
console.log(--secondIncrement);
