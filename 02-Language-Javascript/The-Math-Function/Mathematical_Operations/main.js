// Simple examples of mathematical operations

const number = -8;
const base = 2;
const exponent = 3;

// Absolute value
const absolute = Math.abs(number); // 8

// Power
const power = Math.pow(base, exponent); // 2^3 = 8

// Square root
const squareRoot = Math.sqrt(16); // 4

// Cube root
const cubeRoot = Math.cbrt(27); // 3

// Display results
console.log("Number:", number);
console.log("Absolute value:", absolute);
console.log(`${base} to the power ${exponent}:`, power);
console.log("Square root of 16:", squareRoot);
console.log("Cube root of 27:", cubeRoot);
