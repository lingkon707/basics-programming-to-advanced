// Simple trigonometry example for everyone

// Let's take an angle of 45 degrees and convert to radians
const degrees = 45;
const radians = degrees * (Math.PI / 180);

// Find sine, cosine, and tangent
const sinValue = Math.sin(radians);
const cosValue = Math.cos(radians);
const tanValue = Math.tan(radians);

// Show results
console.log("Angle:", degrees + "Â°");
console.log("Sine:", sinValue);
