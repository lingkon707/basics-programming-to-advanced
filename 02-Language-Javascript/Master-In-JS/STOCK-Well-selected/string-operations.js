// string-operations.js

// Village name operations
let villageName = "Nageshwari";
console.log("Village name:", villageName);

// Convert to uppercase
console.log("Uppercase:", villageName.toUpperCase());

// Convert to lowercase
console.log("Lowercase:", villageName.toLowerCase());

// Get length of village name
console.log("Length of village name:", villageName.length);

// Replace a part of the name
let newVillageName = villageName.replace("Nageshwari", "Nageshwari Bazar");
console.log("Updated village name:", newVillageName);
