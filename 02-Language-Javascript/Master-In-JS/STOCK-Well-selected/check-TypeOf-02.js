// String
let greeting = "Hello, JavaScript!";
console.log("String:", greeting);

// Number
let age = 25;
console.log("Number:", age);

// Boolean
let isStudent = true;
console.log("Boolean:", isStudent);

// Object
let person = { name: "Alice", age: 30 };
console.log("Object:", person);

// Function
function addNumbers(a, b) {
  return a + b;
}
console.log("Function result:", addNumbers(5, 7));

// Objects
// Object
let car = { make: "Toyota", model: "Camry" };
console.log("Object:", car);

// Date
let currentDate = new Date();
console.log("Date:", currentDate);

// Array
let colors = ["red", "green", "blue"];
console.log("Array:", colors);

// String
let message = new String("Hello, JavaScript!");
console.log("String (Object):", message);

// Number
let piValue = new Number(3.14);
console.log("Number (Object):", piValue);

// Boolean
let hasValue = new Boolean(true);
console.log("Boolean (Object):", hasValue);

// Null and Undefined
let nullValue = null;
let undefinedValue;
console.log("Null Value:", nullValue);
console.log("Undefined Value:", undefinedValue);
