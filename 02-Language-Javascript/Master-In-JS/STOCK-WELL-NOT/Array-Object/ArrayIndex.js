const myArray = [10, 20, 30, 40, 50];

// look in below
// Access and log the first element
console.log("First Element:", myArray[0]);

// Access and log the second element
console.log("Second Element:", myArray[1]);

// Change the value of the third element
myArray[2] = 35;
console.log("Modified Third Element:", myArray[2]);

// Calculate the length of the array
const arrayLength = myArray.length;
console.log("Array Length:", arrayLength);

// Access the last element using the length property
console.log("Last Element:", myArray[arrayLength - 1]);
