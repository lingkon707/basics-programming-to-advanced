// Arithmetic Operators
const num1 = 10;
const num2 = 5;
const additionResult = num1 + num2;
const subtractionResult = num1 - num2;
const multiplicationResult = num1 * num2;
const divisionResult = num1 / num2;
const modulusResult = num1 % num2;

console.log("Arithmetic Operators:");
console.log(`Addition: ${additionResult}`);
console.log(`Subtraction: ${subtractionResult}`);
console.log(`Multiplication: ${multiplicationResult}`);
console.log(`Division: ${divisionResult}`);
console.log(`Modulus: ${modulusResult}`);

// Comparison Operator
const x = 5;
const y = 8;

console.log("Comparison Operator:");
console.log(`x > y: ${x > y}`);
console.log(`x < y: ${x < y}`);
console.log(`x >= y: ${x >= y}`);
console.log(`x <= y: ${x <= y}`);
console.log(`x === y: ${x === y}`);
console.log(`x !== y: ${x !== y}`);

// Logical Operators
const isTrue = true;
const isFalse = false;

console.log("Logical Operator:");
console.log(`AND (isTrue && isFalse): ${isTrue && isFalse}`);
console.log(`OR (isTrue || isFalse): ${isTrue || isFalse}`);
console.log(`NOT (!isTrue): ${!isTrue}`);

// Assignment Operators
let a = 5;
let b = 3;

console.log("Assignment Operators:");
console.log(`a = b: ${a = b}`);
console.log(`a += b: ${a += b}`);
console.log(`a -= b: ${a -= b}`);
console.log(`a *= b: ${a *= b}`);
console.log(`a /= b: ${a /= b}`);

// Ternary Operator
const age = 18;
const canVote = age >= 18 ? "Yes" : "No";

console.log("Ternary Operator:");
console.log(`Can vote? ${canVote}`);


// Relational Operators
const apples = 10;
const oranges = 5;

console.log("Relational Operators:");
console.log(`apples > oranges: ${apples > oranges}`);
console.log(`apples < oranges: ${apples < oranges}`);
console.log(`apples >= oranges: ${apples >= oranges}`);
console.log(`apples <= oranges: ${apples <= oranges}`);

// Assignment Operators
let counter = 10;

console.log("Assignment Operators:");
console.log(`Original counter: ${counter}`);
console.log(`+= 5: ${counter += 5}`);
console.log(`-= 3: ${counter -= 3}`);
console.log(`*= 2: ${counter *= 2}`);
console.log(`/= 4: ${counter /= 4}`);
console.log(`%= 3: ${counter %= 3}`);
