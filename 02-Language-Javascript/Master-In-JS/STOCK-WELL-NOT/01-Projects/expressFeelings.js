function expressFeelings() {
    // Loop 5 times to say "I love you"
    for (var i = 0; i < 5; i++) {
        console.log("I love you");
    }

    // Loop 12 times to say "I hate you"
    for (var j = 0; j < 12; j++) {
        console.log("I hate you");
    }
}

// Call the function to execute both loops
expressFeelings();
