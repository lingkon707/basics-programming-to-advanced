let friendName = "Ali";        // string
let age = 15;                  // number
let hasMangoes = true;         // boolean
let friends = ["Ali", "Babu"]; // object (array)

console.log(typeof friendName); // string
console.log(typeof age);        // number
console.log(typeof hasMangoes); // boolean
console.log(typeof friends);    // object


// typeof → Quick check of primitive type (string, number, boolean).
// instanceof → Check object type or class instance.