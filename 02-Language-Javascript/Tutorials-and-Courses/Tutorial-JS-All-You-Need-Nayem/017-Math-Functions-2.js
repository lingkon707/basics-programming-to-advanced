console.log(Math.min(100, 200, 300, 400));
console.log(Math.max(100, 200, 300, 400));

//power
console.log(Math.pow(2, 4)); // {(2*2)*2}*2
console.log(Math.pow(150, 3)); // {(150*150)*150}
console.log(Math.pow(5, 5)); // {(5*5)*5}*5*(5)

// sqrt
console.log(Math.sqrt(25)); // 25 = 5*5
console.log(Math.sqrt(64)); // 64 = 8*8
console.log(Math.sqrt(81)); // 81 = 9*9

// random
console.log(Math.random());
console.log(Math.round(Math.random() * 50 + 1));
