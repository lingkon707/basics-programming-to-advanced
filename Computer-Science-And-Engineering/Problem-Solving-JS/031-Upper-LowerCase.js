 // Program to Convert Lowercase to Uppercase
function convertToUppercase(str) {
    return str.toUpperCase();
}

// Program to Convert Uppercase to Lowercase
function convertToLowercase(str) {
    return str.toLowerCase();
}

// Example usage of convertToUppercase function:
const lowercaseString = "hello";
console.log(convertToUppercase(lowercaseString)); // Output: HELLO

// Example usage of convertToLowercase function:
const uppercaseString = "WORLD";
console.log(convertToLowercase(uppercaseString)); // Output: world
