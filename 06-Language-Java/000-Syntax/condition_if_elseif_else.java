// Chapter: If Else If Ladder
public class condition_if_elseif_else {
    public static void main(String[] args) {
        int score = 75;
        if (score >= 90) {
            System.out.println("Grade A");
        } else if (score >= 60) {
            System.out.println("Grade B");
        } else {
            System.out.println("Fail");
        }
    }
}
